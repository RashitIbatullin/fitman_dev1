void showBodyShapeHelpDialog(BuildContext context, String bodyShape) {
  final helpText = getBodyShapeHelpText(bodyShape);
  showDialog(
    context: context,
    builder: (context) => AlertDialog(
      title: Text(helpText['title']!),
      content: SingleChildScrollView(
        child: ListBody(
          children: <Widget>[
            Text(helpText['description']!),
            const SizedBox(height: 16),
            if (helpText['recommendations']!.isNotEmpty)
              Text(
                'Общие рекомендации:',
                style: Theme.of(context).textTheme.titleMedium,
              ),
            if (helpText['recommendations']!.isNotEmpty)
              Text(helpText['recommendations']!),
          ],
        ),
      ),
      actions: [
        TextButton(
          child: const Text('Закрыть'),
          onPressed: () {
            Navigator.of(context).pop();
          },
        ),
      ],
    ),
  );
}
